package source;

public class Dimensions {
	private final float CELL_ROW = (float) 44.5;
	private final float CELL_COL = (float) 48.75;
	private final int SIZE = 8;
	private final int FRAME_ROW = 900;
	private final int FRAME_COL = 660;
	private final int ICON = 38;
	
	public float getCellRow() {
		return CELL_ROW;
	}
	public float getCellCol() {
		return CELL_COL;
	}
	public int getSize() {
		return SIZE;
	}
	
	public int getFrameRow() {
		return FRAME_ROW;
	}
	
	public int getFrameCol() {
		return FRAME_COL;
	}
	
	public int getIcon() {
		return ICON;
	}
	
}